int main(void)
{
  HAL_Init();

  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM1_Init();

  HAL_TIM_Base_Start(&htim1);

  while (1)
  {
	  move_clockwise(256, 1000);  // 256 half revolution
	  HAL_Delay(100);
	  move_anticlockwise(128, 1000); // 128 quarter revolution
	  HAL_Delay(100);
  }

}
